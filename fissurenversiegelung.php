                                <h3>Fissurenversiegelung</h3>
                                <p class="lead">Rillen und Vertiefungen versiegeln</p>
                                <p>Diese Methode dient ebenfalls einer wirksamen kariesprophylaktischen Vorsorge. Jeder Seitenzahn besteht an seiner Kaufläche aus den sogenannten Höckern und den dazwischen verlaufenden kleinen Furchen, sie werden Fissuren genannt. Und von der längsverlaufenden Längsfissur gehen dann noch viele kleine querverlaufende Querfissuren ab. Dieses Relief an der Kaufläche ihrer Zähne ist bei der Mundhygiene problematisch, denn selbst bei optimaler Zahnputztechnik kann diese Fläche niemals 100% von Plaque befreit werden.</p>

                                <p>Von der Größe her kann man sagen, dass die tiefste Stelle einer Fissur eine deutlich kleinere Öffnung hat als der Durchmesser einer feinen Zahnbürstenborste. In der Tiefe folgen dann nochmal ca. 1 mm. Dieser Fissurengrund ist ideal für die Ansiedlung von Mikroorganismen und damit für die Bildung von Fissurenkaries.</p>

                                <p>Der beste Zeitpunkt für die Versiegelung dieser Fissuren ist ca. 6 Monate nach dem die Zahnkrone vollständig durchgebrochen ist, um im frühern Alter vorzubeugen. Dann ist der Mineralisationsprozess noch nicht vollständig und die Bildung des mineralischen Zahnschmelzes noch nicht abgeschlossen. Die Fissurenversiegelung senkt als vorbeugende Therapiemaßnahme das Risiko für einen kariösen Befall der Seitenzähne um ca. 40 bis 60%. Zur Versiegelung der Fissuren werden dünn fließende, licht-härtende Kunststoffe verwendet. Einige davon setzen Fluoride frei, die ebenfalls die Entstehung von Karies hemmen sollen. Vorzugsweise werden die großen, bleibenden Backenzähne, die sogenannten Molaren, versiegelt. Aber bei Indikation können auch die kleinen bleibenden Backenzähne und die Grübchen der Schneidezähne versiegelt werden.</p>

                                <p>Wir führen in unserer Praxis sowohl die präventive Fissurenversiegelung, als auch die erweiterte Fissurenversiegelung durch, bei der dunkel verfärbte Fissurenanteile vorsorglich entfernt werden. So stellen wir sicher, dass sich unter den Verfärbungen kein bereits angesiedelter Fissurenkaries verbirgt. Nach der Fissurenversiegelung dürfen Sie für ca. 1 Stunde nichts essen oder trinken, nicht die Zähne putzen und kein Kaugummi kauen. Wir empfehlen dann einen halbjährlichen Kontrolltermin bei uns in der Praxis.
                                Bis zum 18. Lebensjahr wird die Fissurenversiegelung an den großen
                                Backenzähnen von den gesetzlichen Krankenkassen übernommen.</p>




