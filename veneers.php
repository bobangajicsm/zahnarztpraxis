                                <h3>Veneers</h3>
                                <p class="lead">Keramikschale für die Zähne.</p>
                                <p>Das sind hauchdünne Keramikschalen mit einer Dicke bis zu 1 mm, die nicht nur der Verschönerung der Zähne dienen sondern auch eine Vielzahl von funktionellen Aufgaben erfüllen. Häufig werden Kronen mit den Veneers verblendet. Damit kann der Schleifverlust am Zahn von 60 auf unter 20% reduziert werden. Dadurch muss der Zahn deutlich weniger abgeschliffen werden und die Zahnsubstanz bleibt zu einem größeren Teil erhalten. Die Schalen werden mit den Zähnen verklebt und verhalten sich dann wie eigener Zahnschmelz und übernehmen auch dessen eigentliche Funktion. Abgeknirschte Zahnsubstanz kann ersetzt werden und verloren gegangene Führungsfunktion wieder hergestellt werden.</p>

                                <p>Die konventionellen Veneers werden aufgesetzt, indem der Zahn ganz leicht abgeschliffen wird. Das Veneers wird zirkulär vom Schmelz begrenzt und kann eingesetzt werden bei Verfärbungen oder kariösen Defekten. Sie werden auf den Frontzähnen aufgeklebt und halten durchschnittlich ca. 10 Jahre. Nicht für Menschen geeignet, die mit den Zähnen knirschen oder wenn die Zähne großflächig kariös sind. Die konventionellen Veneers bestehen aus Keramik oder aus zahnmedizinischem Kunststoff. Die gesetzlichen Krankenkassen übernehmen die Kosten leider nicht, da es sich hier um eine rein kosmetische Behandlung handelt. Die Blenden werden individuell für jeden Patienten angefertigt.</p>

                                <p>Non-Prep Veneers mit einer Dicke von ca. 0,3 mm werden aufgesetzt, wenn die Zähne dauerhaft aufgehellt werden sollen. Ihre Bestandteile sind wie bei den konventionellen Veneers entweder Kunststoff oder Keramik. Sie halten jedoch nur durchschnittlich ca. 5-10 Jahre. Ist ein Non-Prep Veneers beschädigt, kann es nicht repariert werden und muss ausgetauscht werden. Sie sind auch für Kinder geeignet und die Zähne müssen hierfür nicht abgeschliffen werden. Jedoch können dunkle Verfärbungen an den Zähnen durch die Veneers durchschimmern. Die Blenden werden individuell für jeden Patienten angefertigt. </p>

                                <p>Wann kommen keine Venners zum Einsatz:</p>

                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>Wenn Sie häufig mit den Zähnen knirschen</li>
                                    <li>Wenn Sie häufig auf Gegenständen, wie zum Beispiel Kugelschreiber, kauen</li>
                                    <li>Wenn Sie Nägel kaufen</li>
                                    <li>Wenn Sie große Füllungen haben</li>
