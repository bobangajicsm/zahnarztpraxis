                                <h3>Karies Frühdiagnostik</h3>
                                <p class="lead">Karies rechtzeitig behandeln.</p>
                                <p>Bei Karies denken wir immer nur an die kleinen schwarzen Löcher. Aber Karies ist weit mehr und daher ist die Frühdiagnostik ein wesentlicher Bestandteil unserer Arbeit zur Prophylaxe und Ihrer Zahngesundheit. Um das Kariesrisiko eines Patienten einschätzen zu können, ist es sinnvoll, eine diagnostisch fundierte Aussage treffen zu können. Daher brauchen wir möglichst viele Komponenten für eine realistische Beurteilung. Über einen Karies-Risiko-Test identifizieren wir die Karies erzeugenden Bakterien. Insgesamt sind es vier wichtige Speichelparameter, die bestimmt werden müssen:</p>

                                <p>Streptococcus-mutans-Konzentration: Der Leitorganismus beim Zahnkaries und bei fast jedem im Speichel von Natur aus vorhanden. Setzt sich an den Zahnschmelz und gehört zu der Gruppe der Bakterien. Die Konzentration dieser Bakterien im Speichel wird ermittelt.</p>

                                <p>Laktobazillen-Konzentration: Gehören neben den Streptococcus mutans zu den ursächlichen Keimen für die Zahnkaries. Sind besonders für die Demineralisierung der Substanzen aus dem Zahnbein verantwortlich. Das Ergebnis dieses Tests lässt Rückschlüsse auf den Zuckerkonsum des Patienten zu.</p>

                                <p>Salivationsrate (Speichelfluss): Unter Einsatz von Färbemethoden wird die bakteriologische Belastung des Speichels ermittelt und wie gut die Speichelqualität ist. Außerdem wird untersucht, ob eine gestörte Speichelproduktion vorliegt.</p>

                                <p>Pufferkapazität: Im Speichel enthaltene Puffer können Säuren neutralisieren und sind ein wesentlicher Kariesschutz. Wie hoch die Säurebindung ist, wird über den Test der Puffer-Kapazitätsbestimmung erfasst.</p>

                                <p>Auch im 21. Jahrhundert ist trotz aller modernen Geräte, fortschrittlicher Technik und frühzeitiger Aufklärung die Karies noch immer weit verbreitet. Natürlich sind in jeder gesunden Mundflora verschiedene Bakterien anzutreffen. Aber trotzdem erkrankt nicht jeder Mensch auch automatisch an Karies. Die Entstehung von Karies scheint also auf verschiedene Geschehen zurückzuführen sein. Die Struktur des Ökosystems Mundhöhle hat bei jedem Menschen eine andere Zusammensetzung. Grund dafür sind Ernährung, Art und Häufigkeit der Mundhygiene, Oberflächenbeschaffenheit der Zähne sowie der Stoffwechsel.</p>

                                <p>Präventive Strategien zur Reduktion der auslösenden Faktoren ist ein wesentlicher Teil unserer Karies-Prophylaxe. Dazu gehören der regelmäßige Zahnarztbesuch sowie die professionelle Zahnreinigung und selbstverständlich die richtige und regelmäßige Mundhygiene. Die Karies Risikobestimmung dient der frühzeitigen Erkennung eines erhöhten Risikos für Zahnkaries und soll dazu dienen, Sie als betroffenen Patienten umfassend und engmaschig zu betreuen. Nur so lässt sich eine Erkrankung an Karies rechtzeitig abwenden oder therapieren.
                                </p>



