                                <h3>Snore Guards</h3>
                                <p class="lead">For a quiet sleep.</p>
                                <p>A snoring splint displaces the lower jaw forward during sleep, and as a result the upper airways are expanded. The splint is only a means to an end here as it acts only as a counteraction mechanism. The tongue, lower jaw and tissue are placed forward and the bite is slightly opened. The narrowing of the pharynx is reduced, meaning the respiratory tract of the sleeping person is mechanically kept open and the airway resistance is reduced. The snoring splint is also referred to as the protrusion splint and is individually produced in the laboratory from impressions of the teeth. </p>

                                <p>The use of a snoring splint is appropriate for all patients with mild sleep apnea. There are numerous reasons for snoring. Among other things, the following factors may favour sleep apnea.</p>

                                <p>
                                - Narrow airways due to enlarged tonsils and polyps or a large neck circumference <br>
                                - Alcohol and fast food in the evening <br>
                                - Sleeping pills and muscle relaxants <br>
                                - Irregular sleeping times, for example through shift work <br>
                                - Being overweight with a BMI over 30 <br>
                                - Tobacco consumption<br>
                                - Men unfortunately suffer from snoring to a larger extent than women <br>
                                </p>

                               

                                <p>Often snoring is associated with dangerous, but short-term, respiratory lapses. The respiratory lapses and suffocation can completely disappear with a snoring splint. Drowsiness also improves meaning the patient can sleep deeper and better at night. But there are also some factors, with which a snoring guard is not recommended. </p>

                                <p>
                                - With complete toothlessness in the lower jaw <br>
                                - If the bite is poor, in the case of natural underbite <br>
                                - A poor condition of the tooth bed and gums <br>
                                - If there is discomfort in the temporomandibular joint (TMJ) <br>
                                - Inadequate mouth and dental hygiene <br>
                                - If there is restricted ability to open the mouth completely <br>
                                - A strong abrasion of the tooth surfaces <br>
                                </p>

                                <p>The snoring splint is custom-made and attached to the upper and lower jaws. The two parts are connected with a technical part and the lower jaw is held approximately 5-12 mm forward as a result. In up to 80% of cases the so-called obstructive sleep apnea is improved. The dental checks should be carried out every 6 months. This reduces or prevents any side effects such as tension, gagging, bad-taste or mouth dryness. Regular jaw exercises twice a day can also significantly reduce the above-mentioned side effects. This includes exercises such as: Pressing the lower jaw against the palm of the hand with light pressure, or pressing the jaw lightly against the fist.</p>



