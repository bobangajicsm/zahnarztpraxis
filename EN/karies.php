                                <h3>Early tooth decay diagnosis </h3>
                                <p class="lead">Treating tooth decay in sufficient time</p>
                                <p>Tooth decay is always thought of as just small black holes. However, it is much more than just that, and therefore early diagnosis is an essential part of our work and your dental health. In order to be able to assess the tooth decay risk of a patient, it makes sense to be able to make a well-founded diagnostic assessment. Therefore, we need as many components as possible for a realistic assessment. Through a tooth decay risk test, we identify the tooth decay causing bacteria. Overall, there are four important saliva parameters that have to be determined:</p>

                                <p>Concentration of Streptococcus mutans: The organism is present in tooth decay and is naturally present in the saliva of almost everyone.  It attaches itself to the enamel and belongs to the group of bacteria. The concentration of this bacteria in the saliva is determined. </p>

                                <p>Concentration of Lactobacillus: In addition to the Streptococcus mutan, it belongs to the causal germs of tooth decay. It is particularly responsible for the demineralisation of substances from the dentinal (main part of the tooth beneath the enamel). The result of this test allows conclusions to be drawn about the patient’s sugar consumption. </p>

                                <p>Salivation rate (salivary flow): The bacteriological load of the saliva is determined using dyeing methods and testing the quality of the saliva. It is also investigated whether there is abnormal saliva production.</p>

                                <p>Buffering capacity: Buffers contained in the saliva can neutralise acids and are important protection against tooth decay. The height of the acid bond is detected via a test of the buffer capacity determination.</p>

                                <p>Even in the 21st century, despite all modern devices, advanced technology and early detection, tooth decay is still widespread. There are of course different bacteria in every oral flora, but still not every person is automatically infected with tooth decay. The development of tooth decay therefore seems to be due to various events. The structure of the mouth cavity ecosystem has a different composition in every human being. The reasons for this are diet, type and frequency of oral hygiene, surface texture of the teeth as well as the metabolism.</p>

                                <p>Preventive strategies for the reduction of triggering factors is an essential part of tooth decay prevention (prophylaxis). This includes regular visits to the dentist as well as the professional cleaning of teeth and, of course, the correct and regular oral hygiene. The tooth decay risk assessment is used as an early identification of increased risk of tooth decay and is intended to provide our patients with comprehensive and close supervision to avert, or treat tooth decay in good time.</p>


