                                <h3>Orthodontics</h3>
                                <p class="lead">Malpositioning of the teeth and jaw</p>
                                <p>As dentists with advanced orthodontic training, we are able to advise and treat our patients with orthodontic questions. As a branch of dentistry orthodontics deals with the prevention and detection of malpositioning of the teeth and jaw and their treatment. The jaw relation, in other words the relationship between the upper and lower jaw, is shown through a detailed diagnosis and a clinical examination.The orthodontic examination accompanies or prepares the treatment for orthodontists. </p>
                                 
                                <p>Only with a timely diagnosis can the treatment begin as early as possible and the success of the therapy can be increased. Some treatments are only possible in fixed time windows, for example during the eruption of milk teeth, during the entire set of milk teeth, replacement set of teeth, between 12 and 18 years of age by the time of permanent dentition or in adulthood. </p>
                                 
                                <p>The following therapy options are available: </p>
                                 
                                <p><strong>1. Functional Orthodontics, abbreviated as FKO (in German) </strong></p>
                                 
                                <p>The easy (favourable) influence of the jaw positions or movements by active support of the patient. For example an activator, which is a functional orthodontic appliance that trains the patient to push the the underlying lower jaw forward. The activator is removable and is intended to stimulate lower jaw growth beyond the genetically pre-programmed range. </p>
                                 
                                <p><strong>2. Dentofacial orthopedics </strong></p>
                                 
                                <p>Large forces, generally above 500 cN, are said to affect the growth of the upper or lower jaw. </p>
                                 
                                 
                                <p><strong>3. Orthodontics </strong></p>
                                 
                                <p>Here we refer to the movement of the teeth, which takes places with retainers or braces. In the case of retainers (removable devices) the co-operation of the patient is absolutely necessary. Also long after the completion of the growth, orthodontic measures can be followed by orthodontic treatment. </p>
                                 
                                <p><strong>4. Surgical orthodontics </strong></p>
                                 
                                <p>If there is a jaw misplacement present after the growth stage which cannot be remedied by orthodontic measures, then surgery can improve the situation. The treatment is split into three phases: </p>
 
                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>Orthodontic pretreatment with decompensation by means of braces </li>
                                    <li>Surgical intervention, depending on the need of the upper or lower jaw </li>
                                    <li>Orthodontic fine adjustment after the production of the regular jaw position.  </li>
                                </ul>

