                                <h3>Multi tooth gaps</h3>
                                <p class="lead">Several missing teeth</p>
                                <p>If the patient is missing several adjacent teeth, then replacement is only possible using a metal bridge. It is is the only alternative that comes into question for aesthetic reasons, since it is in the colour, shape and extent of the original row of teeth. The surrounding teeth around the multi-tooth gap must be in perfect condition so that they can support the bridge. Metal ceramic bridges are used for larger bridges and give the patient a new lease of life. Implants can also be used, but a very good bone quality is required here. Only this way can the implant sit stably. Furthermore, bone growth must be complete and there may not be any chronic or acute infectious disease present. </p>
                                 
                                <p>Unfortunately, a large number of people are of the opinion that they can still bite well with a gap. We have to show again and again in the dentist surgery to patients the resulting defects on the rest of your teeth, because over time the neighbouring teeth tilt into the existing tooth gap.  Biting only works when a tooth has its opposing tooth as this is the way a stop is reached. In addition, the tooth across from the gap becomes increasingly sensitive to warm and cold. The return of the tooth to its original form is only possible with sophisticated methods and costs a lot of money and time. </p>
                                 
                                <p>The temporomandibular joint (TMJ) also suffers from an untreated multiple tooth gap. Similar to a shock absorber in a car, the jaw can no longer cushion this malfunction when biting. A damaged TMJ can lead to migraine, postural damage and pain. Avoid these serious ailments and come to us in time. Today tooth gaps can be closed again with many different methods. This makes sense not only from an aesthetic point of view but also from a medical point of view. Do not forget that the cost of a larger comprehensive treatment is much higher than “only” a crown or an inlay. It is therefore in your own self interest to go to the dentist early and regularly. Right from the beginning, we advise you professionally and competently and explain everything regarding tooth gaps to you.  </p>

