                                <h3>Fillings</h3>
                                <p class="lead">Sealing defects in the teeth </p>
                                <p>The correct technical term for the filling of holes in the teeth is dental restoration therapy. In colloquial language however, this treatment is often simply referred to as a tooth-filling or filling (or Zahn-Plombe in German). A filling is necessary to salvage a tooth if through tooth decay or after an accident a piece of the tooth must be filled. The initial condition of the tooth determines the type of filling and further treatment; important factors include the hardness and condition of the tooth as well as the position. Not every filling is suitable for each tooth repair. For example, anterior teeth or molars with strong chewing load require separate consideration and treatment.</p>

                                <p>The tooth filling in a tooth affected by tooth decay always has the task of stopping the spread of tooth decay. This process is also called conservative or restorative dentistry. There are different filling materials:</p>

                                <p>1.  Plastic fillings are synthetic tooth-coloured filling material for decayed defects on the front or lateral teeth. Advantages are the supply of tooth-coloured fillings as well as the stabilization of the tooth substance through the bond between the filling and the bones (dentin). </p>

                                <p>2. Compomers in composite dental paints and glass ionomer cement. These are set under UV light and give off a quantity of fluorides, which have a good preventive effect on tooth decay at the filling edge. </p>

                                <p>3. Amalgam was once the most popular filling, but has almost disappeared from practice today. The mercury contained within it is diffused into the mouth air and can then be swallowed through the contaminated saliva or reach the brain via breathing. But for all that, a uniform ban on amalgam fillings is still being debated. Due to the aesthetic and eco-friendly concerns, your Wedding dental practice team will refrain from using amalgam as a filling material. We are an amalgam-free practice. </p>
                                 
                                <p>Plastic fillings of the first two groups can be used for both milk and permanent teeth. Due to our high professional qualifications, we can guarantee that we will attach your tooth filling carefully and safely. This is to prevent the loss of the filling or edge breaks; however, depending on the patient this may still occur. Please come directly to us in the practice so that no secondary tooth decay can form. Regular check ups allow your dentist surgery team to recognize and treat tooth decay. </p>
                                 
                                <p>If the tooth defect is too large to cover with a conventional tooth filling, we will inform you about alternative methods, such as a partial or whole crown. </p>



