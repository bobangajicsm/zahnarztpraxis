                            <h3>Root Canal Treatments</h3>
                            <p class="lead">If the tooth is permanently damaged, it can still be used normally after treatment. </p>
                            <p>If the tooth pulp (the part in the centre of the tooth made up of living connecting tissue and cells) is permanently damaged, this must be removed and sealed with a bacteria-proof root canal filling after disinfection. We carry out root canal treatments in our practice professionally and safely for our patients. The pulpa can sometimes be irreversibly inflamed or already non-vital, meaning it is dead. Intervention in the tooth cavity is always necessary when, based on an X-ray examination, we establish whether:</p>
                            <ul class="bullet-list list-unstyled paddin-bottom-10">

                                <li>The tooth pulp is irreversibly inflamed </li>
                                <li>The tooth pulp is dead </li>
                                <li>A pus-filled pulp inflammation is present or an inflammatory periodontal disease has formed just under the tooth root </li>

                            </ul>

                            <p>In addition to the X-ray image, there are further possibilities for trouble-free diagnostics like the so-called cold test, the heat test, the mechanical sensitivity test as well as the testing of knock sensitivity. </p>
                             
                            <p>The dying of the tooth pulp results in the tooth substance dying off gradually. The tooth pulp contains blood vessels and nerves which supply the tooth from the inside. Reasons for dying can be tooth decay of too great a degree and/or poor oral hygiene. Inflammation is usually preceded by the tooth dying and emerges through pathogens, which can penetrate into deep holes and to the tooth nerves; this is usually caused by tooth decay. </p>
                             
                            <p>Please come to the dentist surgery in good time. A severe toothache is a clear sign and the tooth pulp could already be chronically damaged. As a result we advise you to inform us immediately. Root canal treatment is mainly used to preserve the tooth. Even if the root is dead or the tooth nerve is defunct, the tooth can still be used normally. It is also dangerous to wait too long because the bacteria can also move into the jaw bones and cause serious damage. As well as toothache, a pus-filled inflammation and a fistula on the gum (“gum boil”) are clear warning signs which should not be ignored. </p>


