                                <h3>Amalgam Cleansing</h3>
                                <p class="lead">Removal of amalgam filling</p>
                                <p>As the oldest tooth filling, amalgam is abundant in many mouths. However, the substance has been criticised for many years. It is still used by dentists, but there is currently the plan for amalgam to not be used in pregnant women and children. Controversial discussions and medical research do not result in a uniform result. However, the fact is that some people react sensitively to amalgam fillings.</p>

                                <p>Amalgam repair is an important treatment where the highest safety precautions must be taken. The amalgam often does not pose a risk as long as it is firmly inserted in the tooth. Breaking up the filling causes numerous risks and leads to the release of the substance. As a result, a systematic treatment is enormously important for our patients. The load-free amalgam repair is carried out in 5 steps:</p>

                                <p>1. A cofferdam is used to protect from the inhalation of fumes. Protection against vapours, drilling dust and amalgam residues means they no longer find their way into the mouth and can thus not be swallowed by the patient.<br>
                                2. Using a special vacuum cleaner reduced the exposure to mercury vapour by up to 90%. <br>
                                3. Special extra-thin drills ensure minimal heating and low release of mercury vapour. <br>
                                4. The special drills have a low speed and a special gearing<br>
                                5. When levering out, only as much drilling as necessary is required. The removal of the amalgam fillings takes place as far as possible only by levering out. This method carries the least risk for the formation of hazardous mercury vapour.<br>
                                </p>

                                <p>As an option, we recommend an additional amalgam discharge. Since the half-life of mercury is 20 years, amalgam is still present in the body. This can be due to earlier small defects or discharges of the teeth into the blood circulation or organs, which can lead to a toxic amount. Even if the teeth are freed from amalgam, the targeted elimination of possible amalgam in the body is sensible. We would be happy to advise you on this topic and all other questions regarding amalgam repair in the dental practice.</p>



