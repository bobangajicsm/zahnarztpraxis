                                <p>Teeth whitening has been known about for many centuries, and up to now there have been many means that have been completely ineffective and could even damage the teeth. Previous ​practices ​such as urine of humans and animals, acids or chemicals, have often been very toxic and have permanently damaged the teeth. There are many possibilities today, all of which are pH-neutral and, when used correctly, lighten teeth without damage. </p>
                                 
