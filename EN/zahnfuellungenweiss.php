                                <p class="lead">Tooth-coloured fillings</p>
                                <p>Through the saliva, the oral cavity has a natural defence against tooth decay, but the wrong diet can further exacerbate these problems. This is because the acid attack on the enamel is too intense. The saliva cannot work in this case because it does not have enough time to fulfill its protective function. The saliva is enriched with minerals which strengthen the enamel. Smaller damage is repaired directly. </p>
                                 
                                <p>Want to know which tooth filling is required and which is most suitable? We advise you on all treatments and therapy possibilities for your teeth. The decision for a particular filling therapy is dependent on the tooth that is affected and on possible allergies or personal wishes of the patient. Here is a list of essential criteria of the filling materials: </p>
                                 
                                <p>Amalgam is silvery and best suited for the side teeth. On average, amalgam fillings last about 7-8 years. Patients covered by statutory health insurers are reimbursed for the total cost of treatment by their insurance. Amalgam is not used in our practice. </p>
                                 
                                <p>Glass ionomer cement is dull and light; it is suitable for milk teeth or provisional filling for front or side teeth. They are durable for only about 1-2 years and the total costs are reimbursed for patients covered by statutory health insurance. </p>
                                <p>Compomer fillings are tooth-coloured and also suitable for front and side teeth. The compomer fillings last for around 4-6 years. ​ ​For patients covered by statutory health insurance, 100% of the costs are covered for front teeth, and the cost of the comparable amalgam fillings are covered for side teeth. </p>
                                 
                                <p>Goldinlay fillings are gold-coloured and suitable for side teeth. The average durability is around 10-15 years. Statutory health insurance covers the cost of comparable amalgam fillings. </p>
                                 
                                <p>Ceramic inlay fillings are tooth-coloured and suitable for side teeth. The average durability is approximately 8-10 years. Statutory health insurance covers the cost of comparable amalgam fillings. </p>
                                 
