                                <h3>Veneers</h3>
                                <p class="lead">Ceramic shell for the teeth </p>
                                <p>These are ultra-thin ceramic shells with a thickness of up to 1mm, which not only serve to improve the appearance the teeth, but also perform a variety of functional tasks. Crowns are frequently veneered with veneers. This reduces the grinding loss of teeth from 60% to less than 20%. As a result, the tooth must be significantly less ground down and the tooth substance is retained to a large extent. The shells are glued to the teeth and then behave like their own enamel and assume their proper function. Ground tooth substance can be replaced and lost functionality can be restored. </p>

                                <p>Conventional veneers are easily attached by grinding down the tooth. The veneers cover the enamel and can be used in cases of discolouration or decayed defects. They are glued on the front teeth and last an average of 10 years. Veneers are not suitable for people who grind their teeth or when teeth are extensively decayed. The conventional veneers are made of ceramic or dental plastic. Unfortunately, statutory health insurance does not cover the costs, since this is a purely cosmetic treatment. The veneers are individually made for each patient.</p>

                                <p>When teeth are being brightened permanently, no-prep veneers with a thickness of approximately 0.3 mm are attached. As with conventional veneers, their components are either plastic or ceramic. However, they only last about 5-10 years on average. If a non-prep veneer is damaged, it can not be repaired and must be replaced. They are also suitable for children and the teeth do not have to be ground. However, dark discolourations can shine through the veneers. The veneers are made individually for each patient. </p>

                                <p>No veneers are used in the following circumstances:</p>

                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>If you often grind your teeth </li>
                                    <li>If you frequently chew on items, such as ballpoint pens </li>
                                    <li>If you bite nails</li>
                                    <li>If you have large fillings </li>
                                    <li>If you practise ball or combat sports that could be dangerous to your teeth </li>
