                                <h3>Discolouration</h3>
                                <p class="lead">Intrinsic colour impurities</p>
                                <p>The causes of tooth discolouration are manifold. Rather than bright white, many of us have teeth with yellowish, gray or brownish spots or shades. As a part of facial esthetics, a bright smile with white teeth is an essential part of feeling good and healthy self-confidence. Every human being has a natural tooth colour. Any discolouration that differs from the natural tooth colour is referred to as tooth discolouration. </p>
                                 
                                <p>Dyes from tobacco smoke, cola, tea, coffee or red wine can be the causes of unattractive discolouration. This is not actually about plaque, but rather about colour pigments which are deposited on the surface of teeth. The removal of these discolourations is a cosmetic treatment. Through use of professional tooth cleaning, most of these discolourations can be removed or significantly lightened. </p>
                                 
                                <p>There are also other reasons for discolouration. If enamel becomes thinner, teeth appear yellowish. Iron-containing medication and chromogenic bacteria can also cause teeth to turn unsightly. When the discolouration is from the inside of the tooth, we speak of black discolouration. This comes out from the enamel and is often the result of an accident, tooth decay or root canal treatment. </p>
                                 
                                <p>Teeth with grey tones may have the following causes: </p>
 
                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>Dead pulp (dental pulp) </li>
                                    <li>Discolouration after a fluoride treatment of the teeth </li>
                                    <li>Calcium deficiency </li>
                                    <li>Amalgam fillings </li>
                                    <li>From blood dyes </li>
                                    <li>From ingesting medication such as antibiotics </li>
                                </ul>
                                <p>If you observe a tooth discolouration, then come to us at the Wedding dental practice. We will advise you in detail and find out the causes behind your tooth discolouration. </p>

