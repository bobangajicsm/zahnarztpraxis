                                <h3>Mouthguard</h3>
                                <p class="lead">Custom-fitted to your jaw</p>
                                <p>In many sports, including hockey, football, tennis and combat sports, it is almost inevitable that the teeth get into dangerous situations. In these situations it is essential that the athlete wears a custom-made guard. After all, roughly a third of orofacial injuries occur in sport. Cheek, lip, tongue, crown and root fractures, the shedding of one or more teeth and also jaw fractures can then be avoided.  </p>
                                 
                                <p>A mouthguard can prevent or at least reduce dental injuries. The impact energy is significantly dampened and distributed. The main purpose of a mouth guard is to prevent cuts, fractures and bite injuries. The material for the sports guard must be elastic enough and sufficiently rigid so that the pressure distribution can be optimized. </p>
                                 
                                <p>The individual mouthguard is non-toxic, elastic, absorbs almost no water and is very easy to use. It is custom made with us in the practice. For this we take a plaster print of the upper and lower jaw and make a plaster model of it. The lower jaw is fixed by the mouth guard and it thus protected against impacts. The risk of mild concussion also decreases, since the mouthguard functions as a resilient  damping element between the rows of teeth. The individually manufactured sports guard can be manufactured by us so that it lasts for 2 years, regardless of growth, tooth replacement or orthodontic appliances. </p>
                                 
                                <p>A regular check of the mouthguard is recommended in your dental practice every 6 months. We will check the fit so that the mouth guard ensures safe use and thus also reliable protection during sports. </p>

