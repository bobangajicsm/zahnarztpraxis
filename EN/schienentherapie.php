                                <h3>Splint Therapy </h3>
                                <p class="lead">Effectively treats jaw cracking, teeth grinding and CMD among other things.</p>
                                <p>In the majority of cases it is possible to treat the chewing system with a bite splint. However, splits are also implanted to relieve the temporomandibular joint (TMJ) in chronic or rheumatic diseases. Splint therapy is used for dysfunctional disorders of the chewing system, meaning in bruxism (teeth grinding or pressing) or craniomandibular functional disorders (CMD). CMD is a malfunction between the upper and lower jaw, where the interaction between the upper and lower jaw is disturbed and thus as a result the two jaw joints are put in an awkward position. The muscles are tense and as a result pain can arise in the whole movement apparatus. Tinnitus (ringing in the ears) and limitations in movement of the lower jaw is reported by patients. In addition to physiotherapy and massage, stress treatment and relaxation therapy can also help. If teeth and the jaw are damaged or stressed in such a way that serious consequences could occur, the patient should speak with the dentist about splint therapy.</p>

                                <p>Afflictions and pain can disappear completely or be considerably alleviated with individual therapy and the suitable splint. The so-called bite splint is removable and can be worn at night. It restores the disturbed balance between teeth, muscles and the TMJ. The patient then becomes accustomed to a new chewing behaviour and the TMJ is relieved.</p>

                                <p>However, the removable splint is not always sufficient. For long-term success it is sometimes necessary to carry out a medical procedure. In this procedure, the chewing surfaces are moulded and thus make a new chewing behaviour possible which leads to direct relief of the jaw and teeth</p>

                                <p>In the case of an extensive dental history, we advise each patient individually and in detail. We make sure that: </p>


                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>The tooth system is correct </li>
                                    <li>The mouth opens wide enough </li>
                                    <li>The lower jaw diverges to the side when opening </li>
                                    <li>The TMJ is clicking </li>
                                    <li>The joint gap is elastic </li>
                                </ul>

                                <p>In order to recognize possible diseases and defects early, it is advisable that you regularly visit your dental practice. In 90% of all cases, we can provide you with splint relief and relieve you of any pain and unpleasant consequences.</p>



