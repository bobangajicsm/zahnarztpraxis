                                <h3>Supportive periodontal ther​    apy - UPT</h3>
                                <p class="lead">Periodontitis treating below the gums </p>
                                <p>Within dentistry, UPT is the supportive dental aftercare which should complete the successful treatment of patients with periodontitis. Since treated periodontitis has a life-long risk of resurgence, the UPT serves as an effective way to recognise, stall or completely prevent the resurgence as early as possible. Periodontitis is always due to the presence of germs which have destructive effect on the periodontium.</p>

                                <p>The UPT is carried out by us in the practice and makes it possible to create a definitive status report about your periodontium. For this we require several findings, which we summarise as a periodontal condition. The amount of bacteria which ultimately leads to the onset of disease is also determined by us in the framework of this treatment. Through use of a visible colouring agent, the adhesions and plague on and between your teeth are revealed and then thoroughly removed. Particular attention is paid to the removal of the bacterial deposits in the periodontal pockets and on the root surfaces. Then using a probe we will polish the tooth surfaces and determine the depth of the gingiva at several points of each tooth. The possible tendency to bleed is recorded and filed as a periodontal screening index (PSI) code. In essence, the UPT is a PZR, i.e the professional cleaning of teeth (in German). It is amplified by the PSI code. If you have a pre-existing chronic disorder the resettlement must be prevented or reduced.</p>

                                <p>The periodontal probe has specially tailored length markings ensuring treatment is possible without pain or injury to the gums. The PSI divides the dentition into 6 parts during the examination. The parameters are then collected at 2 or 4 points on each tooth and classified on a scale of 0 to 4. 0 means that the gingiva and periodontium are healthy, while 4 indicates a moderate or severe form of periodontal disease.</p>



                                <p>The individual periodontitis risk is based on the following factors:</p>
                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>Genetic Factors </li>
                                    <li>Oral Hygiene Index </li>
                                    <li>Tooth Loss </li>
                                    <li>Stress </li>
                                    <li>Nicotine Consumption </li>
                                    <li>Systematic Factors, for example diabetes mellitus disease </li>
                                </ul>

                                <p>Supporting periodontal therapy is not covered by statutory health insurance. Costs average between 35 to 150 euros, depending on the number of teeth and overall completion costs.</p>


