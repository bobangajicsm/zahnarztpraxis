                                <h3>Toothless upper or lower jaw</h3>
                                <p class="lead">Wenn der Kiefer keine Zähne mehr hat.</p>
                                <p><strong>Toothless upper jaw</strong></p>
                                <p>In the case of a toothless upper jaw, there are two possibilities of provision with prostheses. This includes:</p>

                                <p>1. Full denture
                                All upper teeth except for the wisdom teeth are replaced with a full denture made of plastic in the toothless upper jaw. The removable denture is reinforced with a metal plate adapted exactly to the palate on which it lies. Patients covered by statutory health insurance are entitled to claim for the full denture.</p>

                                <p>2. Implant-supported denture
                                If implants are possible or present, then prosthesis can use them as points of support. Small magnets are placed on the implants and the denture can be supported or anchored. Examples of good prosthesis positions are the canines and the second small molars. Compared with a total prosthesis, the implant-supported prosthesis has significant advantages in longevity, esthetics and wearing comfort.</p>

                                <p><strong>Toothless lower jaw</strong></p>
                                <p>Total prosthesis are provided in the same way as the toothless upper jaw: Removable implant supported partial denture using a fixed connected bridge on implants just like with the toothless upper jaw. The bridge has a framework made of precious metal alloy and is veneered with tooth-coloured ceramic. Five to six implants are implanted into the toothless lower jaw. The fixed denture is then anchored here in the form of a bridge. The limitation of the implant-supported bridge is that it lies at tooth 36 and 46. </p>
                                 
                                <p>Compared with the full denture and the removable partial implant supported denture, the implant-supported bridge is one of the most comfortable and high-quality prostheses. Unfortunately, this prosthesis is not covered by statutory health insurance. Implants can significantly improve the support of the dentures and offer great wearing comfort. If you have supplementary dental insurance, we are very happy to check if it covers the cost of a implant-supported bridge. Just call us for an appointment, we will gladly provide you with comprehensive advice. </p>

