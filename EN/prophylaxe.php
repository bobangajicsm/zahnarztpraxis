                                <h3>Prophylaxe</h3>
                                <p class="lead">Professional cleaning of teeth</p>
                                <p>The upkeep of our teeth is a necessary ritual - we are particularly keen to ensure that children from an early age regularly brush their teeth. The remaining plaque on teeth is sticky and tough, and if it is not consistently removed it can cause damage to teeth and jaws. Tooth decay is after that the least evil, because the dreaded periodontitis gum disease leads to serious damage to the jaw bone. Only through suitable oral hygiene can it be guaranteed that food remains are thoroughly removed. But toothpaste, mouthwash and dental floss do not accomplish this alone. Therefore, today the unanimous recommendation of dental medicine is to undergo professional cleaning every 6 months. This means the comprehensive and mechanical cleaning of the teeth by specially trained personnel. The difficult to clean interdental space and niches are reached through cleaning and the bacteria are removed. </p>

                                <p>As we get older, the gums recede and expose a portion of the sensitive areas at the necks of the teeth (dental cervix). Especially in these sensitive areas, the bacteria collects in the plaque and can cause unrecoverable damage. Once lost in the gums, it cannot be brought back. The professional cleaning of the teeth, named PZR for short (in German), always involves the complete removal of hidden plaque above and below the gums. The teeth are then treated with a fluoride lacquer, which protects and strengthens the tooth enamel. </p>

                                <p>Patients covered by statutory health insurance must pay for the costs of professional teeth cleaning from their own pockets. However, there are now numerous private endorsement insurances in the benefits catalogue, in which the PZR is included. For young patients aged between 6 and 18 years old, the individual prevention is covered by statutory health insurance. </p>


