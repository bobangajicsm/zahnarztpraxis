                                <h3>Inlays</h3>
                                <p class="lead">Implanted fillings into the teeth</p>
                                <p>Inlays are produced in dental laboratories and are a treatment method for the consequences from tooth decay. They are fitted into the tooth and reconstruct the tooth. An inlay usually consists of gold, ceramic or galvanic ceramic, plastic and, more recently, titanium.  </p>
                                 
                                <p>Plastic alloys are manufactured from composite and look very similar to a natural tooth. The inlays are manufactured in dental laboratories under high pressure and their attachment is similar to the method of ceramic inlay. </p>
                                 
                                <p>Cerec inlays are used after an optical impression of the affected tooth is created with an intraoral camera. The name Ce(ramic) Rec(onstruction) refers to a computer-aided production that creates an image on the monitor with a CAD impression of your teeth. The inlay can then be milled from a plastic block by a robot.  </p>
                                 
                                <p>Ceramic inlays are set with light and then irradiated with halogen light. The ceramic inlay is glued to the tooth. The lifespan is higher than that of the plastic inlay and there is compatibility with the majority of patients without problems. The ceramic inlay is matched so that it blends naturally with the colour of your teeth. Their surface is highly glossed and absolutely resistant. </p>
                                 
                                <p>Gold inlays are very durable and well-tolerated. Large tooth decay defects can also be treated with a gold inlay. However, the gold inlay is more sensitive to heat and cold and this can lead to sensitivity. </p>
                                 
                                <p>Titan inlays have a light silver-grey colour and are veneered with ceramics. The fitting and fixing is carried out in the same way as the conventional ceramic inlays. </p>
                                 
                                <p>Depending on the number of sides of the tooth that are affected, there are three different fillings in the inlays: </p>

                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>One-sided fillings - only the chewing surface of the tooth </li>
                                    <li>Two-sided fillings - the chewing surface of the tooth and the surface to the adjacent tooth Three-sided <li>fillings- the chewing surface of the tooth and both surfaces to the neighbouring teeth </li>
                                </ul>

                                <p>If you have any questions as to whether dental care with inlays is necessary or appropriate in your case, please do not hesitate to call or book an appointment with us. </p>

