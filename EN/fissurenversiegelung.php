                                <h3>Fissure Sealing</h3>
                                <p class="lead">Sealing grooves and pits in the teeth  </p>

                                <p>This approach also acts as effective tooth decay preventative (prophylactic) care. The chewing surface of each side tooth consists of so-called ‘bumps’ with small grooves, called fissures, running between them. Many small transversal grooves come out from the lengthwise extending grooves​.​ This configuration on the chewing surface of your teeth is problematic for oral hygiene, because even with the best brushing technique this surface can never be rid of 100% of plaque. </p>
                                 
                                <p>The size of fissures can be problematic for oral hygiene as their deepest points have a considerably smaller opening than the diameter of a toothbrush bristle since the depth is about 1mm. As a result the fissure base is ideal for the colonisation of microorganisms and thus for the formation of fissure tooth decay. </p>
                                 
                                <p>The ideal time for the sealing of these fissures is about 6 months after the dental crown has completely broken through. This is to prevent it at an early age, since at that point in time, the mineralisation process and the formation of the mineral enamel is not yet complete. Fissure sealing reduces the risk of decay outbreak (attack) of the side teeth by about 40% to 60% more than preventive therapy regimes. For the sealing of the fissures thin-flowing and light-hardening plastics are used. Some of them release fluorides which should also inhibit the development of tooth decay. It is preferable to seal the big permanent molars, however in the case of indication (of a dental issue), the small permanent molars and the dimples of the incisors can also be sealed. </p>
                                 
                                <p>In our practice we carry out both preventive fissure sealing as well as extended fissure sealing, in which darkly coloured fissure parts are removed as a precaution. Through this we ensure that there is no fissure tooth decay hidden under the discolourations. After the fissure seal, you are not allowed to eat, drink, brush your teeth and chew any chewing gum for about an hour. We recommend a semi-annual inspection appointment with us in the practice. Until the age of 18, fissure sealing on the molars is covered by statutory health insurance. </p>

                               

