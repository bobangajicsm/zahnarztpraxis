                                <h3>Crowns and Bridges</h3>
                                <p class="lead">Like your own teeth.</p>
                                <p>The function and the esthetics of the teeth are recovered through prosthetic dentistry. Depending on the requirements, dental restorations can consist of metal alloys, tooth-coloured plastic or ceramics. The ideal case is brought about when the natural tooth structure is not distinguishable from the dentures. Crowns and bridges belong to the fixed dentures. There are the following subtypes: </p>
                                 
                                <p>Partial crowns stabilise the partial regions of the tooth crown and leave the remaining substance of the tooth intact. Where a tooth is infected with several large cavities, the partial crown can be useful to restore the ability to chew. The partial crowns may consist of gold alloys or ceramics. Ceramics have the clear advantage that there is no risk of interactions with the organism. </p>
                                 
                                <p>Full crowns are supported by natural teeth or by implants. For example, the all-ceramic crown is made of ceramic material and completely surrounds the natural tooth crown. </p>
                                Through this, the chewing surface of the tooth is redesigned and encompasses all bumps. The full ceramic crowns are best suited to patients with an intolerance to metal alloys. 
                                 
                                <p>Veneers are made of translucent ceramic and are thin layers which are glued to the tooth surface. The cosmetic appearance of the teeth are optimised by veneers. Veneers are used for cosmetic restoration, and there is no risk of strain on the human body since the material used does not dissolve in solution in the mouth. </p>
                                 
                                <p>Dental bridges are adhesive bridges, split bridges or bridges supported with implants. The adhesive bridges are attached to one or both adjacent teeth; it is not necessary to work extensively using gluing or sanding. Only the part of the tooth surface facing the oral cavity is veneered with an adhesive bridge. The treatment is minimally invasive and is especially used in young teeth. </p>

                                <p>However, different indications have to be clarified in the patient in order to ensure that he is suitable for treatment with an adhesive bridge. To give the necessary best treatment, the adaptation of the crown is usually done in two appointments with us in the dental practice. After new dentures, we recommend a regular visit every 6 months, so we can check the condition of the bridge and keep the risk of tooth decay as low as possible. </p>


