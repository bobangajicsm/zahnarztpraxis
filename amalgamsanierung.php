                                <h3>Amalgam-Sanierung</h3>
                                <p class="lead">Entfernung vo Amalgamplomben</p>
                                <p>Als ältester Füllstoff für Zähne ist Amalgam in vielen Mundhöhlen reichlich vorhanden. Aber bereits seit vielen Jahren ist der Stoff in der Kritik. Noch wird er von uns Zahnärzten verwendet, es gibt aber gerade aktuell den Plan, für Schwangere und Kinder kein Amalgam mehr in der zahnärztlichen Praxis zu verwenden. Kontroverse Diskussionen und medizinische Forschungen kommen nicht zu einem einheitlichen Ergebnis. Fakt aber ist wohl unumstritten, das manche Menschen sensibel auf den Füllstoff Amalgam reagieren.</p>

                                <p>Die Amalgamsanierung ist eine wichtige Behandlung und schon beim ersten Bohren muss auf höchste Sicherheit geachtet werden. Oftmals stellt das Amalgam, solange es fest eingesetzt ist im Zahn, keine Gefahr dar. Das Aufbrechen der Füllung beherbergt zahlreiche Risiken und bedeutet das Freisetzen des Stoffes. Hier ist eine systematische Behandlung enorm wichtig für unsere Patienten. Die belastungsfreie Amalgamsanierung erfolgt in 5 Schritten:</p>

                                <p>1. Zum Schutz vorm Einatmen der Dämpfe kommt ein Kofferdamm zum Einsatz. Schutz gegen Dämpfe und Bohrstaub und Amalgamreste gelangen nicht mehr in den Mundraum und können so nicht vom Patienten verschluckt werden.<br>
                                2. Mit einem Spezialsauger reduziert sich die Belastung durch Quecksilberdämpfe um bis zu 90%.<br>
                                3. Extra dünne Spezialbohrer gewährleisten eine minimale Erhitzung und eine geringe Freisetzung von Quecksilberdämpfen.<br>
                                4. Die Spezialbohrer haben eine niedrige Drehzahl und eine spezielle Verzahnung. <br>
                                5. Beim Aushebeln wird nur so viel gebohrt, wie notwendig. Die Entfernung der Füllungen aus Amalgam finden so weit wie möglich nur durch das Aushebeln statt. Diese Methode birgt das geringste Risiko für die Entstehung von gefährlichen Quecksilberdämpfen.<br></p>

                                <p>Optional empfehlen wir Ihnen noch eine zusätzliche Amalgamausleitung. Da die Halbwertszeit des Quecksilbers bei 20 Jahren liegt, ist noch immer Amalgam im Körper vorhanden. Dies kann über frühere kleine Defekte oder über die Ausleitungen der Zähne in den Blutkreislauf oder den Organen gelangen und zu einer toxischen Belastung führen. Auch wenn die Zähne von Amalgam befreit sind, kann die gezielte Ausleitung des evtl. vorhandenen Amalgams im Körper sinnvoll sein. Sehr gerne beraten wir Sie zu diesem Thema und allen Fragen rund um die Amalgamsanierung in Ihrer Zahnarztpraxis Wedding.</p>



