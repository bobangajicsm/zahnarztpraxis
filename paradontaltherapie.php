                                <h3>Unterstützende Parodontal Therapie – UPT</h3>
                                <p class="lead">Parodontitis unterhalb des Zahnfleisches behandeln.</p>
                                <p>In der Zahnmedizin ist die unterstützende Parodontal Therapie die Nachsorge, die sich nahtlos an die Behandlung eines Patienten mit Parodontitis anschließen soll. Die behandelte Parodontitis beherbergt das lebenslange Risiko eines Wiederaufflammens. Die UPT dient dazu, dieses Wiederaufflammen möglichst früh zu erkennen, zu verzögern oder ganz zu verhindern. Eine Parodontitis beruht immer auf dem Vorhandensein von Keimen, die sich zerstörerisch auf den Zahnhalteapparat auswirken.</p>

                                <p>Die UPT wird bei uns in der Praxis durchgeführt und ermöglicht einen präzisen Zustandsbericht über Ihren Zahnhalteapparat. Dazu benötigen wir mehrere Befunde, die wir in einem Parodontalstatus zusammenfassen. Die Menge an Bakterien, die letztendlich zum Ausbruch der Erkrankung führen, wird von uns im Rahmen dieser Behandlung ebenfalls ermittelt. Nachdem wir die Anhaftungen und Beläge an ihren Zähnen und Zahnzwischenräume mit einem Färbemittel sichtbar darstellen, werden diese gründlich entfernt. Hierbei wird besonders auf die Beseitigung der bakteriellen Auflagerungen in den Zahnfleischtaschen und auf den Wurzeloberflächen geachtet. Anschließend werden wir die Zahnoberflächen polieren und an mehreren Stellen jedes Zahnes die Tiefe der Zahnfleischtasche ermitteln. Dieses geschieht mit einer Sonde. Dabei wird auch die mögliche Blutungsneigung erfasst und als PSI Code erfasst. Im Grunde ist die UPT eine PZR, - die professionelle Zahnreinigung. Erweitert wird sie um den PSI Code,- dem parodontalen Screening-Index . Sollte bereits ein chronischer Verlauf vorliegen, so ist die Wiederbesiedlung zu verhindern bzw. zu verringern.</p>

                                <p>Die spezielle Parodontalsonde hat spezielle Längenmarkierungen und die Behandlung mit der Sonde ist schmerzfrei und ohne Verletzung der Gingiva (Zahnfleisch) möglich. Der PSI,- parodontale Screening-Index teilt bei der Untersuchung das Gebiss in 6 Teile. An jedem Zahn werden nun an 2 bzw. 4 Stellen die Parameter erhoben und auf einer Skala von 0 bis 4 eingeteilt. 0 bedeutet gesundes Gingiva und Parodontium (Zahnbett). Während 4 auf eine mittelschwere bzw. schwere Form der Parodontitis hinweist.</p>



                                <p>Das individuelle Parodontitisrisiko basiert auf folgenden Faktoren:</p>
                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>Genetische Faktoren</li>
                                    <li>Mundhygieneindex</li>
                                    <li>Zahnverlust</li>
                                    <li>Stress</li>
                                    <li>Nikotinkonsum</li>
                                    <li>Systemische Faktoren wie zum Beispiel eine Diabetis mellitus Erkrankung </li>
                                </ul>

