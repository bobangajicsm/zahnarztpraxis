                                <h3>Kieferorthopädie</h3>
                                <p class="lead">Fehlstellungen der Kiefer und Zähne.</p>
                                <p>Als Zahnärzte mit einer kieferorthopädischen Weiterbildung sind wir in der Lage, unsere Patienten in Fragen der Kieferorthopädie zu beraten und zu behandeln. Als Teilgebiet der Zahnmedizin beschäftigt sich die Kieferorthopädie mit der Verhütung und Erkennung von Fehlstellungen der Zähne und des Kiefers und deren Behandlung. Auf Grund einer ausführlichen Diagnostik und einer klinischen Untersuchung wird die Kieferrelation, also die Lagebeziehung zwischen Ober- und Unterkiefer- dargestellt. Die kieferorthopädische Untersuchung begleitet die Behandlung beim Kieferorthopäden oder bereitet diese vor.</p>

                                <p>Nur mit einer rechtzeitigen Diagnose kann die Behandlung so früh wie möglich beginnen und der Therapieerfolg gesteigert werden. Manche Behandlungen sind nur in festgelegten Zeitfenstern möglich, so zum Beispiel während des Milchzahndurchbruchs, beim vollständigen Milchgebiss, Wechselgebiss, zwischen dem 12. und 18. Lebensjahr als permanente Dentition oder im Erwachsenenalter.</p>

                                <p>Folgende Therapiemöglichkeiten gibt es:</p>

                                <p><strong>1. Funktionskieferorthopädie, kurz FKO</strong></p>

                                <p>Die günstige Beeinflussung der Kieferpositionen oder Bewegungen durch aktive Unterstützung des Patienten. Zum Beispiel ein Aktivator, ein funktionskieferorthopädisches Gerät, das den Patienten darauf trainiert, den zurückliegenden Unterkiefer nach vorne zu schieben. Der Aktivator ist herausnehmbar und soll zu einem Unterkieferwachstum anregen, das über das genetisch vorprogrammierte Maß hinausgeht.</p>

                                <p><strong>2. Dentofaziale Orthopädie</strong></p>

                                <p>Große Kräfte, in der Regel über 500 cN, sollen das Wachstum des Ober- oder Unterkiefers beeinflussen.</p>

                                <p><strong>3. Orthodontie</strong></p>

                                <p>Hier sprechen wir vom Bewegen der Zähne, die mit herausnehmbaren oder festen Apparaturen erfolgt. Bei herausnehmbaren Apparaturen ist die Mitarbeit des Patienten zwingend erforderlich. Auch lange nach dem Abschluss des Wachstums, kann mit orthodontischen Maßnahmen eine kieferorthopädische Behandlung erfolgen.</p>

                                <p><strong>4. Chirurgische Kieferorthopädie</strong></p>

                                <p>Wenn nach Wachstumsabschluss eine Kieferfehlstellung vorliegt, die nicht durch orthodontische Maßnahmen behoben werden kann, dann kann eine Operation Verbesserung bringen. Die Behandlung gliedert sich zumeist in drei Phasen:</p>

                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>kieferorthopädische Vorbehandlung mit Dekompensation mittels einer festsitzenden Apparatur</li>
                                    <li>chirurgische Intervention, je nach Notwendigkeit am Ober- oder Unterkiefer</li>
                                    <li>kieferorthopädische Feineinstellung nach der Herstellung der regelrechten Kieferlage.</li>
                                </ul>



