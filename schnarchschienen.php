                                <h3>Schnarchschiene</h3>
                                <p class="lead">Für einen ruhigen Schlaf.</p>
                                <p>Bei einer Schnarchschiene wird der Unterkiefer während des Schlafs nach vorne verlagert und die oberen Atemwege dadurch erweitert. Hier ist die Schiene eigentlich nur Mittel zum Zweck, denn sie dient als Widerlager. Zunge, Unterkiefer und Gewebe werden nach vorne platziert und der Biss leicht geöffnet. Die Verengung des Rachenraums wird vermindert und die Atemwege des Schlafenden wird mechanisch offen gehalten und der Atemwegswiderstand reduziert. Die Schnarchschiene wird auch als Protrusionsschiene bezeichnet und individuell nach den Abformungen der Zähne labortechnisch hergestellt.</p>

                                <p>Für den Einsatz einer Schnarchschiene sind alle Patienten mit einer leichten bis mittleren Schlafapnoe geeignet. Die Gründe für das Schnarchen sind vielfältig. Unter anderem können die folgenden Faktoren die Schlafapnoe begünstigen:</p>

                                <p>- Enge Atemwege auf Grund vergrößerter Mandeln und Polypen oder einem großen Halsumfang<br>
                                - Alkohol und fettes Essen am Abend<br>
                                - Schlafmittel und muskelentspannende Medikamente<br>
                                - Unregelmäßige Schlafzeiten, zum Beispiel durch Schichtarbeit<br>
                                - Starkes Übergewicht mit einem BMI über 30<br>
                                - Tabakkonsum<br>
                                - Männer leider zu einem Großteil eher als Frauen unter dem Schnarchen</p>

                                <p>Oftmals geht das Schnarchen mit gefährlichen, wenn auch kurzzeitigen, Atemaussetzern einher. Die Atemaussetzer und die Erstickungsanfälle können mit der Schnarchschiene vollständig verschwinden. Auch die Tagschläfrigkeit verbessert sich, denn der Patient kann nachts besser und tiefer schlafen. Aber es gibt auch einige Faktoren, bei denen eine Schnarchschiene nicht empfohlen werden kann.</p>

                                <p>- Bei totaler Zahnlosigkeit im Unterkiefer<br>
                                - Bei schlechter Bisslage, wie beim natürlichen Unterbiss<br>
                                - Einem schlechten Zustand des Zahnbetts und des Zahnfleisches<br>
                                - Bei Beschwerden mit dem Kiefergelenk<br>
                                - Wenn weniger als 8 Zähne je Kiefer verblieben sind<br>
                                - Einer unzureichenden Mund- und Zahnhygiene<br>
                                - Bei eingeschränkter Fähigkeit, den Mund vollständig zu öffne<br>
                                - Einem starkem Abrieb der Zahnoberflächen</p>

                                <p>Die Schnarchschiene wird individuell angefertigt und am Ober- und Unterkiefer befestigt. Die beiden Teile werden durch ein technisches Teil verbunden und der Unterkiefer dadurch ca. 5-12 mm nach vorne gehalten. Die sogenannte obstruktive Schlafapnoe verbessert sich in den meisten Fällen um bis zu 80%. Die zahnärztlichen Kontrollen sollten alle 6 Monate stattfinden. Damit werden eventuell auftretende Nebenwirkungen wie Spannungsgefühle, Würgereiz, schlechter Geschmack oder Mundtrockenheit reduziert oder verhindert. Eine regelmäßige Kiefergymnastik zweimal am Tag kann ebenfalls die oben genannten Nebenwirkungen deutlich reduzieren. Dazu gehören Übungen wie zum Beispiel: Den Unterkiefer gegen leichten Druck an der flachen Hand drücken. Oder den Kiefer gegen leichten Druck der Faust drücken.</p>



