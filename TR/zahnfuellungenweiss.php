                                <p class="lead">Diş rengindeki dolgular</p>
                                <p>Tükürük bezleri sayesinde ağız boşluğu çürüklere karşı doğal bür silahtır. Yanlış beslenme sonucu ise, diş minesi üzerine uzun süreki yüksek asit etkisi göstermektedir ve yeterli vakit bulamayan tükürük bezleri, dişleri koruma fonksiyonunu yerine getiremez. Tükürük minerallerle doludur, bu sebeple diş minesindeki küçük zararlar bu mineraller yardımıyla onarılır. </p>

                                <p>Hangi diş dolgusu gereklidir ve hangi malzeme en uygun olanıdır? Dişlerinizin tedavi ve terapi imkanları hakkında sizlere memnuniyetle bilgi veririz. Yapılacak olan dolgu terapisi hastanın dişinin yapısına, olası alerjilere veya hastanın kişisel isteklerine göre şekillenir. Aşağıda, dolgu malzemeleri hakkında önemli kriterleri içeren bir şiste mevcuttur:</p>

                                <p>Amalgam gümüşi renklidir ve en uygun kullanımı yan dişler içindir. Amalgam dolguların ortalama kullanım ömrü yaklaşık 7-8 yıldır. Devlet sigortasına sahip olanların (Almanca: gesetzlich Krankenversicherte) tüm tedavi masrafları sigortalar tarafından karşılanmaktadır. Amalgam dolgu kliniğimizde kullanılmamaktadır. </p>
                                
                                <p>Cam iyonomer Simanlar ise mat ve açık renktedir. Süt dişleri için veya ön veya yan dişlerin geçici dolumu için uygundurlar. Dayanım süreleri sadece 1-2 yıldır ve sigortalar, devlet sigortası sahiplerinin tüm masraflarını karşılamaktadır.</p>
                                Kompomer diş rengindedir ve ön ve yan dişler için uygundur. Kompomer dolgular 4 ila 6 yıl dayanmaktadır. Sigortalar, kompomer dolgu tedavisinde,  amalgam dolguların masrafı tutarında bir meblağı karşılamaktadır.   

                                <p>Kompozit dolgular da diş rengindedir,  ön ve yan dişler için uygundur. Aynı şekilde dayanım süresi 4 ila 6 yıl arasındadır. Devlet sigortası sahipleri için kompozit ön dişlerin ücretinin %100’ü karşılanırken, kompozit yan dişler için amalgam dolguların tutarı kadar bir ücret karşılanmaktadır. </p>

                                <p>Altın dolgular altın rengindedir ve sadece yan dişler için uygundur. Ortalama dayanıklılık süresi 10-15 yıl kadardır. Devlet sigortaları, amalgam dolgu ücreti kadar bir meblağı karşılamaktadır.</p>

                                <p>Seramik dolgular ise diş rengindedir ve yan dişler için uygundur. Ortalama dayanıklılık süresi 8 ila 10 yıl arasında olup, devlet sigortaları bu dolgular için de amalgam dolguların tutarı kadar bir meblağı karşılamaktadır.</p>
                                
                                <p>Eğer devlet sigortasına sahipseniz (Almanca: gesetzlich Krankenversicherter) ve devlet sigortası tarafından %100’ü karşılanmayan bir dolgu yaptırmak istiyorsanız, sizinle bir “Kalan Tutarı Ödemeı” antlaşması yapmaktayız. Bu belge sizin tarafınızdan imzalanır ve geri kalan tutarı sizin üstleneceğinizi belirtir. Sizin üstleneceğiniz tutarı gösteren bir fatura edinirsiniz. Sigortanın karşılayacağı tutarı direkt bağlı olduğunuz sigorta şirketi ile hallederiz. Eğer ek bir diş sigortası poliçesine sahipseniz, lütfen tedavi başlangıcından önce bizleri bilgilendiriniz, sizin için memnuniyetle sigorta kapsamını ve karşıladığı tedavi tutarını inceleriz.</p>
                           
