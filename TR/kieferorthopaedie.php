                                <p><strong>2. Dentofasiyal Ortopedi </strong></p>
                                 
                                <p>500 cN üzerinde uygulanan büyük kuvvetlerin, üst ve alt çene gelişimini etkilemesidir. </p>
                                 
                                 
                                <p><strong>3. Ortodonti</strong></p>
                                 
                                <p>Çıkarılabilir veya sağlam ekipmanların, dişlerin hareketini sağlamasıdır.  Çıkarılabilir ekipmanlarda hastanın işbirliği zorunludur. Büyümenin bitişinden uzun süre sonra bile ortodonti ekipmanları ile çene ortopedi tedavisi mümkündür. </p>
                                 
                                <p><strong>4. Cerrahi Çene Ortopedisi</strong></p>
                                 
