                                <p>Sadece doğru zamanda yapılan bir tanı ile tedavi olabildiğince erken bir vakitte başlayabilir ve başarılı bir terapi gerçekleştirilebilir. Çoğu tedavi önceden belirli bir zaman aralığında gerçekleştirilebilir, örneğin süt dişlerinin düştüğü zaman, dişlerin tamamı süt dişinden oluştuğunda, 12 ila 18 yaş aralığında belirli bir zaman veya yetişkin yaşlarda.</p>
                                 
                                <p>Aşağıdaki terapi imkanları mevcuttur:</p>
                                 
                                <p><strong>1.   Çene Ortopedisi Fonksiyonu, kısaca ÇOF (Almanca: KfO) </strong></p>
                                 
