                                <h3>Destekli Parodontal Terapi – DPT </h3>
                                <p class="lead">Diş etinin altındaki Parodontitis’i tedavi etme </p>
                                <p>Diş hekimliğinde destekli paradontal terapi, tamamlayıcı tedavi olarak, Periodontitis (ilerlemiş dişeti hastalığı) hastalığının tedavisini sorunsuz olarak ilerlemesi için yapılır. Tedavi edilmiş Periodontitisin, ömür boyu yeniden ortaya çıkma riski bulunmaktadır. DPT (Almanca: UPT), bu yeniden ortaya çıkma durumunu olabildiğince erken bir vakitte teşhis etme, erteleme veya tamamen engelleme imkanı sağlar. Parodontitis her zaman, diş köküne zararlı olan çürüklerin varoluşuna dayanır.</p>

                                <p>DPT kliniğimizde uygulanır ve diş kökünüze mükemmel bir sabitlik katar. Parodontal statüyü özetleyebilmek için bir çok bulguya ihtiyaç duymaktayız. Bu bulguları saptamak için gerçekleşen tedavi esnasında, aynı zamanda hastalığa sebebiyet veren bakteri miktarı da belirlenir. Dişlerinizdeki ve diş aralarındaki yapışık katmanları ve birikimleri renklendirici bir araçla açıkça gösterdikten sonra, bu bakteriler iyice temizlenir. Bu esnada, özellikle diş etindeki ve diş kökü yüzeyindeki katmanlaşmış bakterilerin ortadan kaldırılması dikkate alınır. Ek olarak diş yüzeyini parlatılır, dişlerin farklı yüzeylerindeki diş etinin derinliğini ön plana çıkarılır. Bu işlem sonda yardımıyla gerçekleştirilir. İşlem esnasında kanama eğilimi tanımlanır ve bir PSI indeksi ile tanımlanır. Temel olarak DPT, bir çeşit PDT’dir (Profesyonel diş temizleme, Almanca: PZR, - professionelle Zahnreinigung). Bu işlem PSI (Parodontale Screening Index) İndeksine genişletilir. Kronik bir akışın oluşması durumunda, zararlı maddelerin tekrardan yayılması önlenmiş veya azaltılmış olur.</p>

                                <p>Özel parodontal sondası, özel olarak yapılmış uzunluk işaretlerine sahiptir ve sonda ile yapılan tedavi acı vermez ve Gingiva’ya (Diş eti) zarar vermeden işlemi bitirmek mümkündür. PSI (Parodontale Screening Index), diş yapısı incelemesinde 6 parçaya ayrılır. Her diş için 2 ila 4 bölgede parametreler alınır ve 0 ile 4 arasında bir skala ile değerlendirilir. 0, sağlıklı bir Gingiva ve Parodontium (Diş kökü) anlamına gelir. 4 ise Parodontitis’in orta ağırlıkta veya yüksek ağırlıktaki formunu belirtir. </p>



                                <p>Bireysel Parodontitis riski, aşağıdaki faktörlere dayanır:</p>
                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>Genetik Faktörler</li>
                                    <li>Ağız Sağlığı İndeksi</li>
                                    <li>Diş Kaybı</li>
                                    <li>Stres</li>
                                    <li>Diabetis Mellitus (Diyabet, Şeker) Hastalığı gibi Sistematik Faktörler </li>
                                </ul>

