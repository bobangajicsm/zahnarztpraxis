                                <h3>Prophylaxe</h3>
                                <p class="lead">Die Professionelle Zahnreinigung</p>
                                <p>Die Pflege unserer Zähne ist ein wichtiges Ritual, - nicht umsonst legen wir schon bei den Kleinsten auf das regelmäßige Zähneputzen besonderen Wert. Der auf den Zähnen zurückbleibende Belag ist anhaftend und zäh und kann, wenn er nicht immer wieder aufs Neue entfernt wird, zu bleiben Schäden an Zähnen und Kiefer führen. Karies ist dann noch das geringste Übel, denn die gefürchtete Paradontitis führt zu ernsthaften Schäden am Knochen. Nur durch eine geeignete Mundhygiene kann gewährleistet werden, dass Speisereste gründlich entfernt werden. Aber Zahncreme, Munddusche und Zahnseide alleine schaffen diese Aufgabe nicht. Daher ist heute die einstimmige Empfehlung der Zahnmedizin sich alle 6 Monate einer professionellen Zahnreinigung zu unterziehen. Darunter versteht man die umfassende und mechanische Reinigung der Zähne durch speziell ausgebildetes Personal. Die schwer zu reinigenden Zahnzwischenräume und Nischen in der Mundhöhle werden bei der Reinigung erreicht und die Bakterien entfernt.</p>

                                <p>Mit zunehmendem Alter weicht das Zahnfleisch zurück und legt einen Teil der empfindlichen Zahnhälse frei. Besonders an diesen Stellen setzen sich die Bakterien in den Belägen ab und können unwiederbringliche Schäden verursachen. Was einmal verloren ist an Zahnfleisch, kann nicht wieder zurückgebracht werden. Paradontitis ist die Folge und auch der Verlust des Zahns kann drohen. Die professionelle Zahnreinigung ist auch als Erhaltungstherapie nach einer Paradontitisbehandlung sinnvoll. Die professionelle Zahnreinigung, kurz PZR genannt, umfasst immer die vollständige Entfernung versteckter Zahnbeläge über und unterhalb des Zahnfleisches. Anschließend werden die Zähne mit einem speziellen Fluoridlack behandelt, der den Zahnschmelz schützt und stärkt.</p>

                                <p>Die Kosten für die professionelle Zahnreinigung müssen derzeit gesetzlich Krankenversicherte aus eigener Tasche tragen. Es gibt aber inzwischen zahlreiche private Zusatzversicherungen, in deren Leistungskatalog die PZR aufgenommen wurde. Für unsere Kleinen wird die Individualprophylaxe zwischen dem 6. Und dem 18. Lebensjahr von den gesetzlichen Krankenkassen übernommen.</p>


