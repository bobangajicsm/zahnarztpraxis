                                <h3>Zahnfüllungen</h3>
                                <p class="lead">Zahnfarbende Füllungen</p>
                                <p>Durch den Speichel hat die Mundhöhle eine natürliche Abwehr gegen Karies, aber durch die falsche Ernährung etwa, ist der Säureangriff auf den Zahnschmelz zu lange oder zu hoch. Hier kann der Speichel nicht wirken, da er nicht genügend Zeit hat, seine Schutzfunktion zu erfüllen. Der Speichel ist mit Mineralstoffen angereichert, die den Zahnschmelz immer wieder härten. Kleinere Schäden werden also direkt wieder repariert.</p>

                                <p>Welche Zahnfüllung wird benötigt und welches Material ist am geeignetsten? Wir beraten Sie bei allen Behandlungen und Therapiemöglichkeiten rund um Ihr Gebiss. Die Entscheidung für eine bestimmte Füllungstherapie ist abhängig von dem Zahn, der betroffen ist und von möglichen Allergien oder persönlichen Wünschen des Patienten. Hier eine Liste mit den wesentlichen Kriterien der Füllungsmaterialien:</p>

                                <p>Amalgam ist silbrig und am besten für die Seitenzähne geeignet. Durchschnittlich halten Amalgamfüllungen ca. 7-8 Jahre. Gesetzlich Krankenversicherte bekommen die Gesamtkosten der Behandlung von der Krankenkasse erstattet. Amalgam wird in unserer Praxis nicht verwendet.</p>

                                <p>Glasionomerzement ist matt und hell. Geeignet für Milchzähne oder die provisorische Füllung für Front- und Seitenzähne. Haltbarkeit nur ca. 1-2 Jahre und die Krankenkassen erstattet für gesetzlich Versicherte die Gesamtkosten.</p>

                                <p>Kompomer ist zahnfarben und für die Front- und Seitenzähne geeignet. Die Kompomerfüllungen halten ca. 4-6 Jahre. Die Krankenkassen übernehmen die Kosten in Höhe der vergleichbaren Amalgamfüllungen.</p>

                                <p>Komposit ist ebenfalls zahnfarben und auch für die Front- und Seitenzähne geeignet. Auch hier liegt die Haltbarkeit bei ca. 4-6 Jahre. Die gesetzlichen Krankenkassen tragen für ihre Versicherten die Kosten für Frontzähne zu 100% (Einschicht Technik) und bei Seitenzähnen übernimmt sie die Kosten für vergleichbare Amalgamfüllungen.</p>

                                <p>Goldinlay ist goldfarben und nur für die Seitenzähne geeignet. Die durchschnittliche Haltbarkeit liegt bei ca. 10-15 Jahre. Die gesetzlichen Krankenkassen übernehmen die Kosten in Höhe vergleichbarer Amalgamfüllungen.</p>

                                <p>Keramikinlay ist zahnfarben und für die Seitenzähne geeignet. Die durchschnittliche Haltbarkeit liegt bei ca. 8-10 Jahre. Die gesetzlichen Krankenkassen übernehmen die Kosten in Höhe vergleichbarer Amalgamfüllungen.</p>

                                <p>Wenn Sie als gesetzlich Krankenversicherter eine Füllung wünschen, die nicht zu 100% von Ihrer Krankenkasse übernommen wird, dann schließen wir mit Ihnen eine sogenannte Mehrkostenvereinbarung ab. Diese wird von Ihnen unterschrieben und bestätigt Ihre Bereitschaft, die anfallenden Mehrkosten selbst zu tragen. Über diese Mehrkosten erhalten Sie dann von uns eine Rechnung. Den Restbetrag rechnen wir direkt mit Ihrer Krankenkasse ab. Sollten Sie über eine Zahnzusatzversicherung verfügen, so informieren Sie uns bitte vor Therapiebeginn. Gerne prüfen wir die Leistungen und Kostenerstattungen der Versicherung für Sie.</p>



