                                <h3>Inlays</h3>
                                <p class="lead">In den Zähnen eingesetzte Zahnfüllungen.</p>
                                <p>Inlays werden in Dentallabore hergestellt und sind eine Behandlungsmethode der Folgen von Karies. Sie werden in den Zahn eingepasst und rekonstruieren den Zahn. Meist besteht das Inlay aus Gold, Keramik oder galvanischer Keramik, Kunststoff und neuerdings auch aus Titan.</p>

                                <p>Kunststoffinlays werden aus Komposit gefertigt und sehen einem natürlichem Zahn sehr ähnlich. Die Inlays werden in Dentallabore unter hohem Druck gefertigt und ihre Befestigung ist ähnlich der Methode bei einem Keramikinlay.</p>

                                <p>Cerec-Inlays werden eingesetzt, nachdem mit einer intraoralen Kamera ein optischer Abdruck des betreffenden Zahns erstellt wird. Der Name Ce(ramic) Rec(onstruction) weist auf eine computergestützte Herstellung hin. Aus dem Bild am Monitor mit dem CAD Abdruck Ihres Gebisses, lässt sich dann von einem Roboter das Inlay aus einem Kunststoffblock gefräst herstellen.</p>

                                <p>Keramikinlays wird mit Licht ausgehärtet und dann mit Halogenlicht bestrahlt. Das Keramikinlay wird in den Zahn eingeklebt. Die Lebensdauer ist höher als bei einem Kunststoff Inlay und die Verträglichkeit ist bei dem Großteil der Patienten ohne Probleme gegeben. Das Keramikinlay wird farblich an die Zahnfarbe angepasst. Ihre Oberfläche ist hochglänzend und absolut beständig.</p>

                                <p>Goldinlays sind sehr haltbar und gut verträglich. Auch große Kariesdefekte können mit einem Goldinlay versorgt werden. Allerdings ist das Goldinlay empfindlicher gegen Wärme und Kälte und das kann zu Empfindlichkeiten führen.</p>

                                <p>Titaninlays haben eine leicht silbergraue Farbe und werden mit Keramik verblendet. Die Anpassung und die Befestigung erfolgt in der gleichen Art und Weise wie bei den üblichen Keramikinlays.</p>

                                <p>Abhängig von der Anzahl der Seiten des Zahnes, die betroffen sind, gibt es drei unterschiedliche Füllungen bei den Inlays:</p>



                                <ul class="bullet-list list-unstyled paddin-bottom-10">
                                    <li>einflächige Füllungen - nur die Kaufläche des Zahns</li>
                                    <li>zweiflächige Füllungen - die Kaufläche des Zahns und eine Fläche zu einem Nachbarzahn</li>
                                    <li>dreiflächige Füllungen - die Kaufläche des Zahns und beide Flächen zum Nachbarzahn</li>
                                </ul>

                                <p>Haben Sie Fragen, ob aus Ihrer Sicht eine zahnärztliche Versorgung mit Inlays notwendig oder angemessen ist? Dann machen Sie doch am besten direkt einen Termin bei uns oder rufen Sie uns einfach an.</p>

