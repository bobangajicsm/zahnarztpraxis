                                <h3>Zahnfüllungen</h3>
                                <p class="lead">Defekte in den Zähnen verschließen</p>
                                <p>Der fachlich korrekte Begriff für die Füllung für Zahnlöcher ist die Füllungstherapie. Im Volksmund wird diese Behandlung aber auch gerne mit dem Begriff Zahn-Plombe betitelt. Eine Zahnfüllung ist immer dann nötig, wenn durch Karies oder nach einem Unfall ein Stück vom Eigenzahn aufgefüllt werden muss. Dabei ist die Ausgangssituation entscheidend für die Art der Zahnfüllung und die weitere Behandlung. Die Zahnhartsubstanz und der Zahnzustand sowie die Position des Zahnes sind wichtig. Nicht jede Füllung ist für jede Zahnreparatur geeignet. Frontzähne oder Backenzähne mit starker Kaubelastung erfordern eine gesonderte Betrachtung und Behandlung.</p>

                                <p>Die Zahnfüllung bei einem von Karies betroffenen Zahn hat immer die Aufgabe, die Verbreitung von Karies zu stoppen. Diesen Vorgang bezeichnet man auch als Zahnerhaltung. Es gibt unterschiedliche Füllmaterialien:</p>

                                <p>1. Kunststoff-Füllungen, synthetisches Füllungsmaterial in Zahnfarbe, für kariöse Defekte an Front- oder Seitenzähnen. Vorteile sind die Versorgung mit Füllungen in Zahnfarbe sowie die Stabilisierung der Zahnsubstanz durch den Verbund zwischen Füllung und Zahnbein.</p>

                                <p>2. Kompomere in Zahnfarbe aus Komposit und Glasionomerzement. Sie werden unter UV Licht ausgehärtet und geben eine beigefügte Menge Fluoride ab, die eine gute kariesvorbeugende Wirkung am Füllungsrand haben.</p>

                                <p>3. Amalgam war früher der beliebteste Füllstoff, ist aber heute nahezu aus den Praxen verschwunden. Das darin enthaltene Quecksilber wird in die Mundluft diffundiert und kann dann über den kontaminierten Speichel geschluckt erden oder über die Atmung ins Gehirn gelangen. Noch immer wird aber über ein einheitliches Verbot von Amalgamfüllungen gestritten. Aufgrund des ästhetischen und umweltfreundlichen Bedenken verzichtet Ihr Zahnarztpraxis-Wedding Team auf das Amalgam als Füllungsmaterial. Wir sind eine amalgamfreie Praxis.</p>

                                <p>Kunststofffüllungen der beiden ersten Gruppen können sowohl für Milchzähne als auch für die bleibenden Zähne verwendet werden. Durch unsere hohe fachliche Qualifikation können wir Ihnen garantieren, dass wir Ihre Zahnfüllungen besonders schonend und sicher anbringen werden. Das soll verhindern, dass es zu Füllungsverlust oder Randabbrüchen kommt. Natürlich kann dies aber je nach Patient trotzdem mal vorkommen. Bitte kommen Sie dann direkt zu uns in die Praxis, damit sich kein Sekundärkaries bilden kann. Regelmäßige Kontrollen erlauben Ihrem Zahnarztpraxis-Wedding Team die Früherkennung und Behandlung der Karies.</p>

                                <p>Ist der vorliegende Zahndefekt zu groß, um ihn mit einer herkömmlichen Zahnfüllung abzudecken, dann informieren wir Sie über alternative Methoden wie zum Beispiel eine Teil- oder Ganzkrone.</p>




