                                <p class="lead">Verfärbte Zähne aufhellen.</p>
                                <p>Die Zahnaufhellung ist schon seit vielen Jahrhunderten bekannt und bis in die Gegenwart gab es eine Menge Mittel, die völlig ohne Wirkung waren und sogar die Zähne schädigen konnten. Es gab auch wenig praktikable Mittel wie Urin von Mensch und Tier, Säuren oder Chemikalien. Sie waren oftmals sehr giftig und haben den Zahn nachhaltig geschädigt. Heute gibt es viele Möglichkeiten, die allesamt PH-neutral sind und bei sachgemäßer Anwendung ohne Zahnschäden aufhellen.</p>

                                <p>Das Bleaching kann aus medizinischen oder kosmetischen Gründen indiziert sein. Medizinische Gründe können zum Beispiel eine psychische Belastung durch verfärbte Zähne sein oder aber das Aufhellen von wurzelkanalbehandelten Zähnen. Farbgebende Stoffe können in den Zahnschmelz eindringen und sich dunkel verfärben. Das kosmetische Aufhellen der Zähne liegt einem Schönheitsideal zugrunde und kann bis zu einer Stufe auf der Farbskala der Zahnfarbe aufhellen.</p>

                                <p>Bei Zahnverfärbungen geht man von äußeren Einflüssen als Grund aus. Dazu gehören alkoholische Getränke, Tee, Tabakrauch, Kaffee, Rotwein und Fruchtsäfte. Auch Medikamente, Unfälle, Karies oder Mangelernährung führen zu den dunklen Verfärbungen der Zähne. Nicht zu verwechseln sind die Zahnverfärbungen mit den Zahnbelägen. Deswegen beginnt jedes Bleaching mit einer professionellen Zahnreinigung, um die tatsächliche Zahnfarbe besser beurteilen zu können. Außerdem kann so das Bleichmittel besser auf die Zahnsubstanzen einwirken.</p>

                                <p>Beachten Sie bitte, dass Zahnfüllungen, Zahnkronen und Brücken nicht von der Zahnaufhellung verändert werden. Sind diese im sichtbaren Bereich, so kann es zu einem Austausch kommen, damit alle Zähne wieder den einheitlichen Farbton haben. Auch nach einer Aufhellung ist der die Neueingliederung von Zahnersatz schwierig. Diese Problematik erklären wir Ihnen aber ausführlich in der Beratung. Die Zahnaufhellung unterliegt einer Aufklärungspflicht, die wir in unserer Praxis sehr ernst nehmen. Sie werden von uns umfassend beraten und wir informieren Sie über die Risiken, den Ablauf und die möglichen Nebenwirkungen. Übrigens kann eine Zahnaufhellung mehrere Jahre halten. Die Dauer ist aber am Ende abhängig von den Konsumgewohnheiten und der Mundhygiene jedes Einzelnen. Auffrischungsbehandlungen sind immer dann notwendig, wenn die aufgehellten Zähne im Ganzen oder partiell merklich dunkler werden.</p>



