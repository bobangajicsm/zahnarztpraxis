                                <p class="lead">Wenn der Kiefer keine Zähne mehr hat.</p>
                                <p><strong>Zahnloser Oberkiefer</strong></p>
                                <p>Bei einem zahnlosen Oberkiefer gibt es zwei Möglichkeiten einer Versorgung mit Prothesen. Dazu gehören:</p>

                                <p>1. Totalprothese
                                Mit einer Totalprothese aus Kunststoff werden im zahnlosen Oberkiefer alle Zähne, bis auf die Weisheitszähne, ersetzt. Die herausnehmbare Prothese ist mit einer Metallbasis verstärkt und liegt auf dem Gaumen auf. Diesem wurde die Vollprothese exakt angepasst. Gesetzlich Krankenversicherte haben einen Anspruch auf die Vollprothese</p>

                                <p>2. Implantatgetragene Prothese
                                Wenn Implantate möglich sind oder vorhanden sind, dann können Prothesen diese als Stützpunkte nutzen. Auf die Implantate werden kleine Magnete aufgesetzt und mit deren Hilfe kann der Zahnersatz abgestützt oder verankert werden. Beispiele für gute Prothesen Positionen sind die Eckzähne und die zweiten kleinen Backenzähne. Die implantatgetragene Prothese hat gegenüber einer Totalprothese deutliche Vorteile in der Langlebigkeit, der Ästhetik und dem Tragekomfort.</p>

                                <p><strong>Zahnloser Unterkiefer</strong></p>
                                <p>Totalprothese genau wie beim zahnlosen Oberkiefer
                                Herausnehmbare Teilprothese auf Implantaten genau wie beim zahnlosen Oberkiefer
                                Festsitzende durch Steg verbundene Brücke auf Implantaten.
                                Die Brücke hat ein Gerüst aus Edelmetall Legierung und wird mit Keramik in Zahnfarbe verblendet. Im zahnlosen Unterkiefer werden fünf bis sechs Implantate eingesetzt. Hier wird der festsitzende Zahnersatz in Form einer Brücke dann verankert. Die Begrenzung der durch Steg verbundenen Brücke auf Implantaten liegt bei Zahn 36 und 46.</p>

                                <p>Verglichen mit der Totalprothese und der herausnehmbaren Teilprothese auf Implantaten gehört die festsitzende auf Steg verbundene Brücke auf Implantaten zu den angenehmsten und hochwertigsten Prothesen. Leider ist diese Prothese nicht Teil der Versorgung für gesetzlich Krankenversicherte. Implantate können den Halt des Zahnersatzes noch deutlich verbessern und bieten einen sehr hohen Tragekomfort. Sollten Sie eine Zahnzusatzversicherung besitzen so schauen wir sehr gerne nach, ob diese einen Teil der Kosten für eine Prothese auf Implantaten übernimmt. Rufen Sie einfach für einen Termin bei uns an, wir beraten Sie gerne umfassend.
