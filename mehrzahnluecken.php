                                <p>Fehlen dem Patienten mehrere nebeneinanderliegende Zähne, dann bleibt nur der Ersatz über eine Metallbrücke. Sie ist die einzige Alternative, die aus ästhetischen Gründen in Frage kommt, da sie in Farbe, Form und Umfang der ursprünglichen Zahnreihe. Die umgebenden Zähne um die Mehrzahnlücke müssen einwandfrei in Takt sein, damit sie die Brücke tragen können. Metallkeramik Brücken werden für größere Brücken verwendet und geben dem Patienten ein neues Lebensgefühl zurück. Auch Implantate können gesetzt werden, jedoch ist hier eine sehr gute Knochenqualität zwingend erforderlich. Nur so kann das Implantat stabil sitzen. Außerdem muss das Knochenwachstum abgeschlossen sein und es darf keine chronische oder akute Infektionskrankheit vorliegen.</p>

