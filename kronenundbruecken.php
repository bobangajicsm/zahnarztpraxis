                                <h3>Kronen und Brücken</h3>
                                <p class="lead">Wie die eigenen Zähne.</p>
                                <p>Mit der prothetischen Zahnmedizin soll die Funktion und die Ästhetik der Zähne wieder hergestellt werden. Je nach Anforderungen kann Zahnersatz aus Metalllegierungen, aus zahnfarbenem Kunststoffe oder Keramik bestehen. Der Idealfall ist dann erreicht, wenn der natürliche Zahnbestand nicht von dem Zahnersatz zu unterscheiden ist. Kronen und Brücken gehören zu dem festsitzenden Zahnersatz. Es gibt die folgenden Unterarten:</p>

                                <p>Teilkronen, die die Teilbereiche der Zahnkrone stabilisieren und die noch verfügbare Substanz des Zahns unversehrt lassen. Ist ein Zahn mit mehreren großen Karieslöchern befallen, dann kann die Teilkrone sinnvoll sein, um die Kaufähigkeit wieder herzustellen. Die Teilkronen können aus Goldlegierungen bestehen oder aus Keramik. Keramik hat hier den klaren Vorteil, dass keine Wechselwirkungen mit dem Organismus zu befürchten sind.</p>

                                <p>Vollkronen, die von den natürlichen Zähnen getragen werden oder von Implantaten. Zum Beispiel die Vollkeramikkrone, die aus keramischem Material besteht und die natürliche Zahnkrone vollständig ummantelt. Hiermit wird die Kaufläche des Zahnes neu gestaltet und umfasst sämtliche Höcker. Für Patienten mit einer Unverträglichkeit gegen Metalllegierungen sind die Vollkronen aus Keramik bestens geeignet.</p>

                                <p>Veeners, die Verblendschalen aus lichtdurchlässiger Keramik ist eine hauchdünne Schicht, die auf die Zahnoberfläche aufgeklebt wird. Die Optik der sichtbaren Oberflächen der Zähne wird durch die Veeners optimiert. Sie dienen der kosmetischen Restaurierung und bei der Verwendung von Veeners besteht kein Risiko zur Belastung des menschlichen Organismus, da das verwendete Material nicht im Mund in Lösung übergeht.</p>

                               <p>Brücken als Klebebrücken oder geteilte Brücke oder mit Implantaten. Die Klebebrücken werden an einem oder beiden Nachbarzähnen befestigt. Hierzu muss nicht aufwendig durch Verkleben oder Anschleifen gearbeitet werden. Lediglich der zur Mundhöhle liegende Teil der Zahnfläche wird mit einer Klebebrücke verblendet. Die Behandlung ist minimalinvasiv und wird vor allem im jugendlichen Gebiss angewendet. Hierfür sind allerdings verschiedene Indikationen beim Patienten abzuklären, um zu gewährleisten, dass er für die Behandlung mit einer Klebebrücke geeignet ist. Für die Anpassung der Krone wird in der Regel in zwei Terminen bei uns in der Zahnarztpraxis alles nötige unternommen, um Sie bestens zu behandeln. Wir empfehlen nach dem neuen Zahnersatz einen regelmäßigen Besuch bei uns, ca. alle 6 Monate. So können wir den Zustand der Brücke überprüfen und das Kariesrisiko so gering wie möglich halten.</p>





